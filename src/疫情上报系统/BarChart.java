package 疫情上报系统;
import java.awt.Font;
import org.jfree.chart.ChartFactory;
import org.jfree.chart.ChartPanel;
import org.jfree.chart.JFreeChart;
import org.jfree.chart.axis.CategoryAxis;
import org.jfree.chart.axis.ValueAxis;
import org.jfree.chart.plot.CategoryPlot;
import org.jfree.chart.plot.PlotOrientation;
import org.jfree.data.category.CategoryDataset;
import org.jfree.data.category.DefaultCategoryDataset;
import java.awt.GridLayout;
import java.sql.Connection;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

import javax.swing.JFrame;
public class BarChart {
	ChartPanel frame1;
	public  BarChart(){
		CategoryDataset dataset = getDataSet();//将获得的数据传递给CategoryDataset类的对象
       JFreeChart chart = ChartFactory.createBarChart3D(
      		                 "疫情情况统计图", // 图表标题
                           "疫情感染情况", // 目录轴的显示标签
                           "确诊感染人数", // 数值轴的显示标签
                           dataset, // 数据集
                           PlotOrientation.VERTICAL, // 图表方向：水平、垂直
                           true, // 是否显示图例(对于简单的柱状图必须是false)
                           false,  // 是否生成工具
                           false  // 是否生成URL链接
                           );
      
       CategoryPlot plot=chart.getCategoryPlot();//获取图表区域对象
       CategoryAxis domainAxis=plot.getDomainAxis();         //水平底部列表
        domainAxis.setLabelFont(new Font("黑体",Font.BOLD,14));         //水平底部标题
        domainAxis.setTickLabelFont(new Font("宋体",Font.BOLD,12));  //垂直标题
        ValueAxis rangeAxis=plot.getRangeAxis();//获取柱状
        rangeAxis.setLabelFont(new Font("黑体",Font.BOLD,15));
         chart.getLegend().setItemFont(new Font("黑体", Font.BOLD, 15));
         chart.getTitle().setFont(new Font("宋体",Font.BOLD,20));//设置标题字体

        frame1=new ChartPanel(chart,true);  //这里也可以用chartFrame,可以直接生成一个独立的Frame
        
	}
	   private static CategoryDataset getDataSet() {
	        boolean result = false;
	        Connection conn = DatabaseConnection.getConn();
	        try {
	          //统计语句
	        	Statement sta = conn.createStatement();
	        	   //使用statement对像执行sql语句
	        	   ResultSet rs=sta.executeQuery("SELECT count(*) as 疫情统计人员总数,sum(case when Sinfect='是' then 1 else 0 end)确诊人员总数  FROM Student");
	        	   while(rs.next()){	        
	        	          DefaultCategoryDataset dataset = new DefaultCategoryDataset();
	        	          dataset.addValue((Number) rs.getObject(1), "总人数", "总人数");
	        	          dataset.addValue((Number) rs.getObject(2), "确诊人数", "确诊人数");
	        	          return dataset;
	        } 
	        }
	        	   catch(Exception e) {
	            e.printStackTrace();
	        } finally {
	            try {
	                conn.close();
	            } catch (SQLException e) {
	                e.printStackTrace();
	            }
	        }
			return null;

	   }
	   public ChartPanel getChartPanel(){
		   return frame1;
	
	   	}
	   public static void main(String args[]){
		   JFrame frame=new JFrame("疫情感染情况统计图");
		   frame.setLayout(new GridLayout(2,2,10,10));
		   frame.add(new BarChart().getChartPanel());   
		   frame.setBounds(0, 0, 900, 800);
		   frame.setVisible(true);
	   }
}

